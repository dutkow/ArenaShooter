    PLAYER_TEAM_CHANGED,
    PLAYER_READY_STATUS_CHANGED,
    SERVER_CLOSED,
    RECEIVED_CONNECTING_PLAYER_INFO,
    PLAYER_KICKED,
    BACKUP_NOMINATION,
    BACKUP_ASSIGNED,
    PLAYER_RESIGNED,
    ALL_PLAYERS_LOADED,
    START_GAME_COUNTDOWN,
    START_GAME,
    GAME_FINISHED,
    PLAYER_PING_UPDATE,
    GAME_PHASE_CHANGED,
    CHAT_MESSAGE,
}

