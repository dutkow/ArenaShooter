using System;

[Flags]
public enum ENetPacketFlags : int
{
    None = 0,
    Reliable = 1,
    Unsequenced = 2,
    UnreliableFragment = 8
}

// Server sees messages from clients
public enum ClientMsg : byte
{
    CONNECTION_REQUEST,
    CLIENT_LOADED,
    PLAYER_INFO,
    CHANGE_PLAYER_NAME,
    CHANGE_PLAYER_NUMBER,
    CHANGE_PLAYER_TEAM,
    CHANGE_READY_STATUS,
    SEND_CONNECTING_PLAYER_INFO,
    CHAT_MESSAGE,
    INPUT,
}

// Client sees messages from server
public enum ServerMsg : byte
